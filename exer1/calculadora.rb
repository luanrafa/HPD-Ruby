def soma(num1, num2)
    resultado = num1 + num2
    puts "#{num1} + #{num2} = #{resultado}"
end

def sub(num1, num2)
    resultado = num1 - num2
    puts "#{num1} - #{num2} = #{resultado}"
end

def mult(num1, num2)
    resultado = num1 * num2
    puts "#{num1} * #{num2} = #{resultado}"
end

def div(num1, num2)
    resultado = num1.to_f / num2.to_f
    puts "#{num1} / #{num2} = #{resultado}"
end